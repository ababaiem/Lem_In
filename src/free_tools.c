#include "../include/lem_in.h"

void free_links(t_lemin *env)
{
  t_link *tmp;
  t_link *tmp2;

  tmp = env->links_map;
  while (tmp)
  {
    tmp2 = tmp->list_next;
    free(tmp);
    tmp = tmp2;
  }
}
